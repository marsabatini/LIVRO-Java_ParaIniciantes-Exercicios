public class Exemplo0004 {

    public static void main (String args[]){

        double gallons; // contém o número de galoes
        double liters; // contém o numero de litros

        gallons = 10; // variável comeća com 10 galões

        liters = gallons * 3.7854; // converte os galões em litros

        System.out.println(gallons + " gallons is " + liters + " liters.");

    }
}
